package com.example.demo.VPTree.benchMark;

public class CartesianPoint {
    private final double x;
    private final double y;

    public CartesianPoint(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }
}
